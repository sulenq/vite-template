// src/scripts/generate-file-path-header.cjs

const fs = require("fs");
const path = require("path");

const rootDir = process.cwd();
const srcDir = path.join(rootDir, "src");

if (!fs.existsSync(srcDir)) {
  console.error(
    "Error: src directory not found. Please run this script from the project root.",
  );
  process.exit(1);
}

// Helper to recursively get files
function getFiles(dir, fileList = []) {
  const files = fs.readdirSync(dir);
  for (const file of files) {
    const name = path.join(dir, file);
    if (fs.statSync(name).isDirectory()) {
      getFiles(name, fileList);
    } else {
      fileList.push(name);
    }
  }
  return fileList;
}

const allFiles = getFiles(srcDir);

// Filter out autogenerated directories/files
const filesToProcess = allFiles.filter((file) => {
  const relPath = path.relative(rootDir, file);

  // Exclude paraglide and routeTree.gen.ts
  if (
    relPath.startsWith("src/paraglide/") ||
    relPath === "src/routeTree.gen.ts"
  ) {
    return false;
  }

  // Include .ts, .tsx, .js, .jsx, .cjs, .css
  const ext = path.extname(file);
  return [".ts", ".tsx", ".js", ".jsx", ".cjs", ".css"].includes(ext);
});

console.log(`Found ${filesToProcess.length} files to process.`);

let updatedCount = 0;

for (const file of filesToProcess) {
  const relPath = path.relative(rootDir, file);
  let content = fs.readFileSync(file, "utf8");

  const ext = path.extname(file);
  const isCss = ext === ".css";

  // Define what the header comment should look like
  const expectedComment = isCss ? `/* ${relPath} */` : `// ${relPath}`;

  let lines = content.split("\n");
  let hasUpdated = false;

  if (isCss) {
    const firstLine = lines[0] ? lines[0].trim() : "";
    const commentRegex = /^\/\*\s*src\/.*?\s*\*\/$/i;
    if (commentRegex.test(firstLine)) {
      if (lines[0] !== expectedComment) {
        lines[0] = expectedComment;
        hasUpdated = true;
      }
    } else {
      lines.unshift(expectedComment, "");
      hasUpdated = true;
    }
  } else {
    const firstLine = lines[0] ? lines[0].trim() : "";
    const commentRegex = /^\/\/\s*src\/.*/i;
    if (commentRegex.test(firstLine)) {
      if (lines[0] !== expectedComment) {
        lines[0] = expectedComment;
        hasUpdated = true;
      }
    } else {
      lines.unshift(expectedComment, "");
      hasUpdated = true;
    }
  }

  if (hasUpdated) {
    const newContent = lines.join("\n");
    fs.writeFileSync(file, newContent, "utf8");
    console.log(`Updated: ${relPath}`);
    updatedCount++;
  }
}

console.log(`Successfully updated ${updatedCount} files.`);
